public class BreederVO {
    private String id;
    private String name;
    private String icon;
    private String level;
    private String score;
    private String nour;
    private String dogCoin;
    private String promotionBenefit;
    private String contractGain;
    private String totalGain;
    private String onlineState;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getNour() {
        return nour;
    }

    public void setNour(String nour) {
        this.nour = nour;
    }

    public String getDogCoin() {
        return dogCoin;
    }

    public void setDogCoin(String dogCoin) {
        this.dogCoin = dogCoin;
    }

    public String getPromotionBenefit() {
        return promotionBenefit;
    }

    public void setPromotionBenefit(String promotionBenefit) {
        this.promotionBenefit = promotionBenefit;
    }

    public String getContractGain() {
        return contractGain;
    }

    public void setContractGain(String contractGain) {
        this.contractGain = contractGain;
    }

    public String getTotalGain() {
        return totalGain;
    }

    public void setTotalGain(String totalGain) {
        this.totalGain = totalGain;
    }

    public String getOnlineState() {
        return onlineState;
    }

    public void setOnlineState(String onlineState) {
        this.onlineState = onlineState;
    }
}
